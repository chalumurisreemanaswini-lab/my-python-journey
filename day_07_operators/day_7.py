# 1. Comparition (True or False?)

print(10 > 5)     # Greater than: True
print(10 < 5)     # Less than: False
print(10 == 10)   # Equal to: True (Notice the double ==)
print(10 != 5)    # Not equal to: True

# 2. String Comparision (Case sensitive!)
print("apple" == "Apple") # False (The capital 'A' makes it different)

# 3. PRACTICAL
anna_age = 16
required_age = 18
print("Am I old enough to drive?", anna_age >= required_age)


