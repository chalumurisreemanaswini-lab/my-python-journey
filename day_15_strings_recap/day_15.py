# 5. String indexing
word = "Python"

print(word[0])   # P
print(word[3])   # h
print(word[-1])   # n

# 6. String slicing
word = "Python"

print(word[0:4])   # Pyth
print(word[2:])   # thon
print(word[:3])   # Pyt

# 7. Common string methods
text = "I love python"

print(text.upper())
print(text.capitalize())
print(text.count("o"))
print(text.replace("python", "coding"))

