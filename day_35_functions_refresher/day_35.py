# 1. Default Values in Functions
# You can give a default value to parameter, so if someone doesn't provide it, Python uses the default

def greet(name="Friend"):
  print("Hello", name, "!")

greet("Sam")     # Hello Sam !
greet()          # Hello Friend !

# - name="Friend" = if no argument is given, use "Friend"

