# --- DAY 26: NUMBER ADDER PROGRAM (WHILE LOOP PRACTCE)

# 1. The program
total = 0
number = 1

while number != 0:
  number = int(input("Enter a number (0 to stop): "))
  total += number
  print("Current total:", total)

print("Final total:", total)

