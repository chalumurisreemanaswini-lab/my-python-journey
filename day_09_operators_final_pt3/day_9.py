# --- DAY 9: GEOMETRY AND CALCULATIONS ---

# 1. Calculating the perimeter of a triangle
# Let's pretend we a measuring a physical object
side_a = 5
side_b = 10
side_c = 8
perimeter = side_a + side_b + side_c

print("The perimeter of the triangle is:", perimeter)

# 2. Calculating the area of a rectangle
length = 20
width = 10
area = length * width

print("The area of the rectangle is:", area)

# 3. Using Comparision + Math
# Is area larger than 150
is_large_rectangle = area > 150
print("Is this a large rectangle?", is_large_rectangle)

