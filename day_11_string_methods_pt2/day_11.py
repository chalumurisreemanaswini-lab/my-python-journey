# DAY 11: THE "TRANSFORMATION" BUTTONS

# 1. The "SHOUTING" Button (.upper())
# This turns everything into capital letters.

message = "i am learning"
print(message.upper())    # RESULT: I AM LEARNING

# 2. The "whispering" Button (.lower())
# This turns everything into lowercase (great for fixing messy user input)

name = "ALEX"
print(name.lower())   # RESULT: alex

# 3. The "Length" Button (len())
# This counts every letter and space

word = "Python"
print(len(word))   # RESULT: 6

