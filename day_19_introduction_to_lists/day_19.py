# --- DAY 19: INTRODUCTION TO LISTS ---

# Creating a list
fruits = ["apple", "banana", "orange"]

print(fruits)

# Acessing list items (just like strings)
fruits = ["apple", "banana", "orange"]
print(fruits[0])   # apple
print(fruits[-1])   # orange

# 3. Length of a list
fruits = ["apple", "banana", "orange"]
print(len(fruits))   # 3

# 4. Adding items to a list
fruits = ["apple", "banana"]
fruits.append("orange")
print(fruits)

# Looping through a list
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
  print(fruit)

